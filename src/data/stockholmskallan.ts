/**
 * Returns curated Stockholmskällan photos that match the given decade.
 */
export function getStockholmskallanPhotos(year: number): UnifiedPhoto[] {
  const from = year;
  const to = year + 9;
  return STOCKHOLMSKALLAN_PHOTOS.filter((p) => p.year != null && p.year >= from && p.year <= to);
}
